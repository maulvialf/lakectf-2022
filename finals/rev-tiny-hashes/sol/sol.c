// k&r 1
uint32_t kr1(uint32_t h, char c) {
    h += c;
    return h;
}

// k&r 2
uint32_t kr2(uint32_t h, char c) {
    h = c + 31*h;
    return h;
}

// djb
uint32_t djb(uint32_t h, char c) {
    h = ((h << 5) + h) + c; /* hash * 33 + c */
    return h;
}



void print_alphabet(int hash_idx) {
  size_t sz = (sizeof(HASHES)/(3*sizeof(uint32_t)));
  printf("size %d\n", sz);
  for(int i = 0; i < sz; i++) {
    int idx = HASHES[i][hash_idx] % sizeof(ALPHABET);
    printf("[%u, %d, '%c'],\n", HASHES[i][hash_idx], idx, ALPHABET[idx]);
  }
}


